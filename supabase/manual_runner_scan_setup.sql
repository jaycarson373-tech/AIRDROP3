create unique index if not exists scout_signals_mint_idx on public.scout_signals (mint);
create unique index if not exists scout_signals_one_active_idx
  on public.scout_signals ((status)) where status = 'active';
create index if not exists scout_signals_public_idx
  on public.scout_signals (public_at desc) where status in ('active', 'passed', 'archived');
create index if not exists scout_signals_detected_idx
  on public.scout_signals (detected_at desc);

create table if not exists public.scout_signal_events (
  id bigint generated by default as identity primary key,
  signal_id uuid references public.scout_signals(id) on delete cascade,
  event_type text not null,
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists scout_signal_events_signal_idx
  on public.scout_signal_events (signal_id, created_at desc);

create table if not exists public.scout_settings (
  key text primary key,
  value jsonb not null,
  description text,
  updated_at timestamptz not null default now()
);

