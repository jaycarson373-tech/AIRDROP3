create extension if not exists pgcrypto;

create table if not exists public.scout_signals (
  id uuid primary key default gen_random_uuid(),
  chain text not null default 'solana',
  mint text not null unique,
  name text not null,
  symbol text not null,
  source text not null default 'manual',
  source_chat_id text,
  source_message_id text,
  source_url text,
  status text not null default 'passed'
    check (status in ('queued', 'active', 'passed', 'rejected', 'archived')),
  scout_score numeric check (scout_score is null or scout_score between 0 and 100),
  price_usd numeric,
  market_cap_usd numeric,
  liquidity_usd numeric,
  volume_24h_usd numeric,
  holder_count integer,
  token_age_seconds integer,
  metrics jsonb not null default '{}'::jsonb,
  reasons text[] not null default '{}',
  risk_flags text[] not null default '{}',
  selection_reason text,
  detected_at timestamptz not null default now(),
  selected_at timestamptz,
  public_at timestamptz not null default now(),
  retired_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

