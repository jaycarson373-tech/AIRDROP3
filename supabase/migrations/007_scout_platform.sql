drop policy if exists "public reads released scout signals" on public.scout_signals;
create policy "public reads released scout signals"
  on public.scout_signals for select
  using (public_at <= now() and status in ('active', 'passed', 'archived'));

drop policy if exists "public reads released scout events" on public.scout_signal_events;
create policy "public reads released scout events"
  on public.scout_signal_events for select
  using (
    exists (
      select 1 from public.scout_signals signal
      where signal.id = signal_id
        and signal.public_at <= now()
        and signal.status in ('active', 'passed', 'archived')
    )
  );

drop policy if exists "public reads scout settings" on public.scout_settings;
create policy "public reads scout settings"
  on public.scout_settings for select
  using (key in ('eligibility_minimum', 'public_signal_delay_seconds', 'distribution_bps', 'protocol_reserve_bps'));

grant select on public.scout_signals, public.scout_signal_events, public.scout_settings to anon, authenticated;
