create or replace function public.scout_activate_signal(target_signal_id uuid, reason text default null)
returns public.scout_signals
language plpgsql
security definer
set search_path = public
as $$
declare
  activated public.scout_signals;
begin
  update public.scout_signals
  set status = 'passed', retired_at = now(), updated_at = now()
  where status = 'active' and id <> target_signal_id;

  update public.scout_signals
  set status = 'active', selected_at = coalesce(selected_at, now()), retired_at = null,
      selection_reason = coalesce(reason, selection_reason), updated_at = now()
  where id = target_signal_id
  returning * into activated;

  if activated.id is null then
    raise exception 'Scout signal % not found', target_signal_id;
  end if;

  insert into public.scout_signal_events (signal_id, event_type, payload)
  values (target_signal_id, 'activated', jsonb_build_object('reason', reason));

  return activated;
end;
$$;

